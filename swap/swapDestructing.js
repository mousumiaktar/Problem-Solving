//JavaScript program to swap two variables
let a = 1;
let b = 2;

//using destructuring assignment
[a, b] = [b, a];

console.log(`The value of a = ${a}`);
console.log(`The value of b = ${b}`);





//JavaScript program to swap two String-------------
